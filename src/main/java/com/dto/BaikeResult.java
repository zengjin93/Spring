package com.dto;

public class BaikeResult extends ResultBean {
    
    private String baseContent;
    
    private String moreLink;
    
    private String searLink;
    
    private String baikeLink;

    public String getBaseContent() {
        return baseContent;
    }

    public void setBaseContent(String baseContent) {
        this.baseContent = baseContent;
    }

    public String getMoreLink() {
        return moreLink;
    }

    public void setMoreLink(String moreLink) {
        this.moreLink = moreLink;
    }

    public String getSearLink() {
        return searLink;
    }

    public void setSearLink(String searLink) {
        this.searLink = searLink;
    }

    public String getBaikeLink() {
        return baikeLink;
    }

    public void setBaikeLink(String baikeLink) {
        this.baikeLink = baikeLink;
    }
    
    

}
